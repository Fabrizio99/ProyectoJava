    public static boolean validarRegistroID(String ID){
        boolean validador=false;
        for (int i = 0; i < registroID.size(); i++) {
            if(ID.equals(registroID.get(i))){
                validador=true;
                break;
            }
        }
        return validador;
    }
    public static boolean validarLongitudID(String ID){
        boolean validador=false;
        if(ID.length()!=8){
            validador=true;
        }
        return validador;
    }
